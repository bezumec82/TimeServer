void IL0373::setLutsFull(void)
{
    sendCmd(VCOM_LUT);
    for(size_t count = 0;
            count < sizeof(vcom_lut_arr);
            count++) {
        sendData(vcom_lut_arr[count]);
    }

    sendCmd(W2W_LUT);
    for(size_t count = 0;
            count < sizeof(w2w_lut_arr);
            count++) {
        sendData(w2w_lut_arr[count]);
    }

    sendCmd(B2W_LUT);
    for(size_t count=0;
            count < sizeof(b2w_lut_arr);
            count++) {
        sendData(b2w_lut_arr[count]);
    }

    sendCmd(W2B_LUT);
    for(size_t count=0;
            count < sizeof(w2b_lut_arr);
            count++) {
        sendData(w2b_lut_arr[count]);
    }

    sendCmd(B2B_LUT);
    for(size_t count=0;
            count < sizeof(b2b_lut_arr);
            count++) {
        sendData(b2b_lut_arr[count]);
    }
}

